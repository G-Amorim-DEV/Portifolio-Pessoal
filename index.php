      </p>
    </article>

    <article class="uc mb-3">
      <h3>UC2 – Construção de Aplicações Front-End</h3>
      <p>
        Aprofundei meus conhecimentos em HTML, CSS e JavaScript, aprendendo a importância de criar interfaces intuitivas, acessíveis e responsivas. Compreendi conceitos como design mobile-first, responsividade com Bootstrap, boas práticas de experiência do usuário e acessibilidade seguindo princípios WCAG. O front-end deixou de ser apenas sobre estética e passou a ser sobre proporcionar experiências que sejam claras, funcionais e inclusivas para qualquer pessoa e dispositivo.
      </p>
    </article>

    <article class="uc mb-3">
      <h3>UC3 – Publicação de Aplicações Web</h3>
      <p>
        Nesta unidade, aprendi a transformar um projeto local em uma aplicação disponível para o mundo. Trabalhei com hospedagem gratuita e profissional, configuração de servidores, limitações de ambientes compartilhados e organização de arquivos para produção. Entendi como funciona o deploy, quais cuidados tomar e como diagnosticar erros após a publicação. Foi uma das UCs que mais expandiu minha visão prática do mercado.
      </p>
    </article>

    <article class="uc mb-3">
      <h3>UC4 – Construção de Aplicações Back-End</h3>
      <p>
        Aqui tive contato profundo com a lógica do back-end. Aprendi sobre PHP, rotas, requisições HTTP, sessões, autenticação, validações e construção de APIs. Comecei a entender como os dados trafegam entre o front e o back e como garantir segurança e integridade durante esse processo. Também aprendi a organizar projetos de forma modular e estruturada, aplicando boas práticas de desenvolvimento.
      </p>
    </article>

    <article class="uc mb-3">
      <h3>UC5 – Banco de Dados</h3>
      <p>
        Uma das áreas com que mais me identifiquei. Estudei modelagem relacional completa, normalização, criação e manutenção de tabelas, consultas avançadas com SQL (incluindo JOINs e funções de agregação), índices para performance e integração com PHP. Descobri como o banco é a base de qualquer aplicação e como uma modelagem bem feita pode facilitar ou complicar todo o desenvolvimento.
      </p>
    </article>

    <article class="uc mb-3">
      <h3>UC6 – Serviços Web e Integrações</h3>
      <p>
        Nesta unidade, mergulhei no mundo das APIs, aprendendo sobre padrões REST, formatos de dados, autenticação via tokens, consumo e criação de APIs e comunicação entre sistemas. Entendi como integrações permitem que duas aplicações diferentes conversem e trabalhem juntas, abrindo um universo de possibilidades. APIs são o coração de muitos sistemas modernos e entender isso mudou minha visão como desenvolvedor.
      </p>
    </article>

    <article class="uc mb-3">
